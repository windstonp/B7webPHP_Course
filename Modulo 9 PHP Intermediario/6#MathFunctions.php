<?php

    //abs retorna o valor absoluto
    echo abs(-10);

    echo "<hr>";

    //round arrendonda um numero baseado no numero
    echo round(1.6);

    echo "<hr>";

    //ceil arrendonda pra cima
    echo ceil(2.1);

    echo "<hr>";

    //floor o contrario do ceil
    echo floor(27.99);

    echo "<hr>";

    //rand retorna um inteiro aleatorio entre um numero e outro
    echo rand(0,100);

    echo "<hr>";

?>